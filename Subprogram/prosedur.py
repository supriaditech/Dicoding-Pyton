# contoh prosedur
# Prosedur adalah suatu blok kode yang dapat dijalankan berulang-ulang dengan menggunakan nama prosedur dan parameter-parameter yang diperlukan.
# Prosedur tidak memiliki nilai kembalian, sehingga tidak dapat digunakan sebagai nilai suatu variabel.
# Perbedaan antara prosedur dan fungsi adalah, fungsi memiliki nilai kembalian, sedangkan prosedur tidak memiliki nilai kembalian.
def greeting(name):
    print("Halo " + name + ", Selamat Datang!")


